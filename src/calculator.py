class Calculator :
    # return the sum between 2 operands
    def mysum(self, first_operand, second_operand):
        return first_operand + second_operand
    def multiply(self, first_operand, second_operand):
        return first_operand * second_operand
    def my_mean(self, operand_list):
	    return sum(operand_list) / len(operand_list)
